    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String kad1 = kad.getText().trim();
        String kyazar2 = kyazar.getText().trim();
        String kyayınevi3 = kyayınevi.getText().trim();
        try {
            String query = "INSERT INTO `kitaplar` (`kid`, `kad`, `kyazar`,`kyayınevi`) VALUES (NULL, ?, ? ,?);";
            PreparedStatement pre = db.preBaglan(query);
            pre.setString(1, kad1);
            pre.setString(2, kyazar2);
            pre.setString(3, kyayınevi3);
            int statu = pre.executeUpdate();
            if(statu > 0) {
                kitaplar kitap = new kitaplar();
                kitap.setVisible(true);
                dispose();
            }
        } catch (Exception e) {
            System.err.println("insert error : " + e);
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

